
public class Bank {

	//Attribut
	private String BankName;
	private int BankKredit;
	
	//Getter
	public String getBankName() {
		return BankName;
	}
	public int getBankKredit() {
		return BankKredit;
	}
	
	//Setter
	public void setBankName(String bankName) {
		BankName = bankName;
	}
	
	public void setBankKredit(int bankKredit) {
		BankKredit = bankKredit;
	}

	
	
}
